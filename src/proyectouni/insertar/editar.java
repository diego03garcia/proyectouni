    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
    conexion mysql =new conexion();
    Connection cn=mysql.conectar();
        
    String nom,pat, mat, gra, gen, dir="";
    int edad,tel, alum=0;
    String sSQL="";
    
    alum=Integer.parseInt(txtalum.getText());
    nom=txtnom.getText();
    pat=txtpat.getText();
    mat=txtmat.getText();
    gra=cbxgra.getSelectedItem().toString();
    edad=Integer.parseInt(txteda.getText());
    gen=cbxgen.getSelectedItem().toString();
    tel=Integer.parseInt(txttel.getText());
    dir=txtdir.getText();
    sSQL="update alumno "
            +"set idalumno=?,"
            +"nombre=?,"
            +"apaterno=?,"
            +"amaterno=?,"
            +"grado=?,"
            +"edad=?,"
            +"genero=?,"
            +"telefono=?,"
            +"direccion=?"
            +"where idalumno="+alum+";";
    try{
        PreparedStatement pst= cn.prepareStatement(sSQL);
        pst.setInt(1, alum);
        pst.setString(2, nom);
        pst.setString(3, pat);
        pst.setString(4, mat);
        pst.setString(5, gra);
        pst.setInt(6, edad);
        pst.setString(7, gen);
        pst.setInt(8, tel);
        pst.setString(9, dir);
        int n= pst.executeUpdate();
        if(n>0){
        JOptionPane.showMessageDialog(this,"El registro se edito correctamente","Confirmacion",JOptionPane.INFORMATION_MESSAGE);
        
        }
    }catch(Exception e){
        
    }
            
         
    }//GEN-LAST:event_btnguardarActionPerformed

