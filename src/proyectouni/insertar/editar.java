    public void cargarTabla(String valor){
        String sSQL="";
        try{
          modelo= new DefaultTableModel(null,titulos);  
          conexion mysql= new conexion();
          Connection cn= mysql.conectar();
          sSQL="Select idalumno,nombre,apaterno,amaterno from alumno where CONCAT(idalumno,'',nombre,'',apaterno,'',amaterno)like'%"+valor+"%';";
          try{
            Statement st= cn.createStatement();
            ResultSet rs=st.executeQuery(sSQL);
            while(rs.next()){
              datos[0]=rs.getString("idalumno");
              datos[1]=rs.getString("nombre");
              datos[2]=rs.getString("apaterno");
              datos[3]=rs.getString("amaterno");
              modelo.addRow(datos);
            }
            tblmodificar.setModel(modelo);
          }catch(SQLException ex){
            JOptionPane.showMessageDialog(this, "Error"+ex, "Error", JOptionPane.ERROR_MESSAGE); 
          }
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Error"+e, "Error", JOptionPane.ERROR_MESSAGE);
        }
        
    }
    
    void editarRegistro(String valor){
        String sSQL="";
        String nom="", pat="", mat="", gen="", dir="", gra="";
        int tel=0, alum=0, edad=0;
      
        try{
          conexion mysql =new conexion();
          Connection cn= mysql.conectar();
          sSQL= "Select*from alumno where idalumno="+valor+";";
          try{
             Statement st= cn.createStatement();
             ResultSet rs= st.executeQuery(sSQL);
             while(rs.next()){
               alum=rs.getInt("idalumno");
               nom=rs.getString("nombre");
               pat=rs.getString("apaterno");
               mat=rs.getString("amaterno");
               gra=rs.getString("grado");
               edad=rs.getInt("edad");
               gen=rs.getString("genero");
               tel=rs.getInt("telefono");
               dir=rs.getString("direccion");
             }
             txtalum.setText(""+alum);
             txtnom.setText(nom);
             txtpat.setText(pat);
             txtmat.setText(mat);                         
             txttel.setText(""+tel);
             txtdir.setText(dir);
             txteda.setText(""+edad);
             if(gra.equals("Primero")){
                cbxgra.setSelectedIndex(1);
             }else if(gra.equals("Segundo")){
                cbxgra.setSelectedIndex(2); 
             }else if(gra.equals("Tercero")){
                cbxgra.setSelectedIndex(3);     
             }else if(gra.equals("Cuaro")){
                cbxgra.setSelectedIndex(4);    
             }else if(gra.equals("Quinto")){
                cbxgra.setSelectedIndex(5);    
             }else{
                cbxgra.setSelectedIndex(6);     
             }
             
             if(gen.equals("Masculino")){
               cbxgen.setSelectedIndex(1);
             }else{
               cbxgen.setSelectedIndex(2);  
             }
             
                      
          }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Error"+e, "Error", JOptionPane.ERROR_MESSAGE);  
          }
        }catch(Exception e){
           JOptionPane.showMessageDialog(this, "Error en la conexion"+e, "Error", JOptionPane.ERROR_MESSAGE);
        }
                
}

